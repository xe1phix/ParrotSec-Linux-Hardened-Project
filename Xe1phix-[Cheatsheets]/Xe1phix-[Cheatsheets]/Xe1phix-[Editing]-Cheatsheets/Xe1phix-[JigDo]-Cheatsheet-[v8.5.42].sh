assembles them into an exact copy of your image (using `rolling checksum' concept')
jigdo downloads the parts and reassembles the image.

jigdo-file-cache.db is a Berekeley DB file containing md5sums of any files read in

The template file is a binary file generated by jigdo-file,
it contains information on how to reassemble the image

the script finishes by calculating a checksum of the generated image








