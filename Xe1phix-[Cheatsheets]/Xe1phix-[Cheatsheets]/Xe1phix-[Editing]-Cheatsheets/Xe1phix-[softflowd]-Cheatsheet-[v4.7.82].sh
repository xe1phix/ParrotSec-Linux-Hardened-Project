#!/bin/sh


##-==============================================================================-##
##   [+] listen on interface eth0 and to run in statistics gathering mode only
##-==============================================================================-##
softflowd -i eth0


##-=====================================================================================================-##
##   [+] export NetFlow v.5 datagrams on flow expiry to a flow collector running on 10.1.0.2 port 4432
##-=====================================================================================================-##
softflowd -i eth0 -n 10.1.0.2:4432


##-==============================================================================-##
##   [+] export NetFlow v.5 datagrams on flow expiry
##   [+] flow collector running on 10.1.0.2 port 4432 and 10.1.0.3 port 4432
##-==============================================================================-##
softflowd -i eth0 -n 10.1.0.2:4432,10.1.0.3:4432


##-=====================================================================================================-##
##   [+] flow collector running on 10.1.0.2 port 4432 and 10.1.0.3 port 4432 with load balncing mode
##   [?] Odd netflow packets will be sent to 10.1.0.2 port 4432 
##   [?] even netflow packets will be sent to 10.1.0.3 port 4432.
##-=====================================================================================================-##
softflowd -i eth0 -l -n 10.1.0.2:4432,10.1.0.3:4432


##-=========================================================================================-##
##   [+] increases the number of concurrent flows that softflowd will track to 65536 and 
##   [+] increases the timeout for UDP flows to 90 seconds.
##-=========================================================================================-##
softflowd -v 5 -i eth0 -n 10.1.0.2:4432 -m 65536 -t udp=1m30s


##-=====================================================================-##
##   [+] export NetFlow v.9 flows to the multicast group 224.0.1.20. 
##   [+] The export datagrams will have their TTL set to 64
##-=====================================================================-##
## --------------------------------------------------------------------- ##
##   [?] multicast receivers can be many hops away.
## --------------------------------------------------------------------- ##
softflowd -v 9 -i eth0 -n 224.0.1.20:4432 -L 64


## ------------------------------------------------------------------ ##
##   [?] alternate locations for the control socket and pid file
## ------------------------------------------------------------------ ##
softflowd -i eth0 -p /var/run/sfd.pid.eth0 -c /var/run/sfd.ctl.eth0


