The "isosize" program can be used to find out the
appropriate amount of bytes to be read from the optical media.

/sbin/isosize -x <device>


Then "sector count" and "sector size" are passed to "dd" to read
the appropriate amount of bytes from the optical media and the byte stream
is then piped to the appropriate checksum tool (sha512sum, sha256sum, etc).


dd if=<device> count=<sector count> bs=<sector size> | sha512sum

